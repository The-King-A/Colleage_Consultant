    public record Profile(
            String description,
            String courseList,
            String careerDirection,
            String graduateDestinations,
            String typicalEmployers,
            String typicalCareers) {

        Profile withName(String name) {
            String desc = description;
            if (desc != null && desc.contains("计算机科学与技术")) {
                desc = desc.replace("计算机科学与技术", name);
            } else if (desc != null && !desc.startsWith(name)) {
                desc = name + "专业" + desc.substring(desc.indexOf("专业") + 2);
            }
            return new Profile(desc, courseList, careerDirection, graduateDestinations, typicalEmployers, typicalCareers);
        }
    }

    public static Profile resolve(String code, String name, String category, String subcategory) {
        Profile specific = byCode(code);
        if (specific != null) {
            return specific;
        }
        return byCategory(name, category, subcategory);
    }

