    private static Profile byCategory(String name, String category, String subcategory) {
        if (subcategory != null) {
            if (subcategory.contains("计算机")) {
                Profile base = byCode("080901");
                return base != null ? base.withName(name) : engineering(name);
            }
            if (subcategory.contains("电子信息") || subcategory.contains("通信")) {
                Profile base = byCode("080701");
                return base != null ? base.withName(name) : engineering(name);
            }
            if (subcategory.contains("机械")) {
                return mechanical(name);
            }
            if (subcategory.contains("土木") || subcategory.contains("建筑")) {
                return civil(name);
            }
            if (subcategory.contains("工商管理") || subcategory.contains("会计")) {
                return business(name);
            }
            if (subcategory.contains("临床医学") || subcategory.contains("药学") || subcategory.contains("护理")) {
                return medical(name, subcategory);
            }
        }
        return switch (category) {
            case "工学" -> engineering(name);
            case "管理学" -> management(name);
            case "经济学" -> economics(name);
            case "法学" -> law(name);
            case "医学" -> medical(name, subcategory);
            case "文学" -> literature(name);
            case "理学" -> science(name);
            case "教育学" -> education(name);
            case "艺术学" -> arts(name);
            default -> generic(name, category);
        };
    }

